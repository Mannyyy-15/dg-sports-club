// Language switcher
function setLanguage(lang) {
  localStorage.setItem('dg_lang', lang);
  document.documentElement.setAttribute('lang', lang === 'hi' ? 'hi' : lang === 'mr' ? 'mr' : 'en');

  // Update all elements with data-translate attribute
  document.querySelectorAll('[data-translate]').forEach(el => {
    const key = el.getAttribute('data-translate');
    if (translations[key] && translations[key][lang]) {
      el.textContent = translations[key][lang];
    }
  });

  // Update language selector buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.remove('bg-primary', 'text-white');
    btn.classList.add('text-primary', 'hover:bg-primary/10');
    if (btn.dataset.lang === lang) {
      btn.classList.add('bg-primary', 'text-white');
      btn.classList.remove('text-primary', 'hover:bg-primary/10');
    }
  });
}

// Auto-init on DOM ready
document.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('dg_lang') || 'en';
  setLanguage(saved);
});
